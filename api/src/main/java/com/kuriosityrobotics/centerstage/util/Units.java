package com.kuriosityrobotics.centerstage.util;

public class Units {
	/**
	 * Constant to convert centimetres to base unit of length (metres)
	 */
	public static final double CM = .01;
	public static final double M = 1;

	/**
	 * Constant to convert inches to base unit of length (metres)
	 */
	public static final double IN = 2.54 * CM;

}
